func TestRenderGeneratedConfigYAMLIncludesIdentities(t *testing.T) {
	t.Parallel()

	config := model.DefaultAuditConfig()
	config.CommandTimeout = 5 * time.Second
	config.Profile.Name = "example-host"
	config.Profile.OSFamily = "debian"
	config.Profile.Paths.AppScanRoots = []string{"/var/www"}
	config.Identities = model.IdentityConfig{
		DeployUsers:   []string{"forge"},
		RuntimeUsers:  []string{"forge"},
		RuntimeGroups: []string{"forge"},
		WebUsers:      []string{"www-data"},
		WebGroups:     []string{"www-data"},
	}

	contents, err := renderGeneratedConfigYAML(config, "setup")
	if err != nil {
		t.Fatalf("renderGeneratedConfigYAML() error = %v", err)
	}

	rendered := string(contents)
	if !strings.Contains(rendered, "Generated by larainspect setup") {
		t.Fatalf("expected generated header, got %q", rendered)
	}
	if !strings.Contains(rendered, "identities:") || !strings.Contains(rendered, "deploy_users:") {
		t.Fatalf("expected identities section, got %q", rendered)
	}
	if !strings.Contains(rendered, "www-data") {
		t.Fatalf("expected rendered web identity, got %q", rendered)
	}
}

